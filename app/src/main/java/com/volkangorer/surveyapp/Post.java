public class Post {
    String surveyname;
    String explanation;
    String date1;
    String date2;
    String id;

    public Post(String surveyname, String explanation, String date1, String date2, String id) {
        this.surveyname = surveyname;
        this.explanation = explanation;
        this.date1 = date1;
        this.date2 = date2;
        this.id = id;
    }
}
